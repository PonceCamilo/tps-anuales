package Interesado;

public class DatosInteresado {
    String nombre;
    String apellido;
    String mail;
    int dni;
    Double pasaporte;

    //constructor
    public DatosInteresado (String nombre, String apellido, String mail, int dni, Double pasaporte){
        this.nombre = nombre;
        this.apellido = apellido;
        this.mail = mail;
        this.dni = dni;
        this.pasaporte = pasaporte;
    }
}
